package com.nepharm.apps.fpp.biz.pem.bean;

/**
 * KPI 指标参数 对象
 * @author lizhen
 *
 */
public class KPIParamBean {
	private String csid; //原参数ID
	private String key; //参数名
	private String type;//类型
	private double value;//参数值
	
	public KPIParamBean(String key,String type,double value){
		this.key=key;
		this.type=type;
		this.value=value;
	}
	public KPIParamBean(String csid,String key,String type,double value){
		this.csid=csid;
		this.key=key;
		this.type=type;
		this.value=value;
	}
	
	
	public String getCsid() {
		return csid;
	}
	public void setCsid(String csid) {
		this.csid = csid;
	}
	public String getKey() {
		return key;
	}
	public void setKey(String key) {
		this.key = key;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public double getValue() {
		return value;
	}
	public void setValue(double value) {
		this.value = value;
	}
}
