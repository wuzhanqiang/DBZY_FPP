package com.nepharm.apps.fpp.biz.pem.bean;

/**
 * 绩效填写-子表对象
 * @author lizhen
 */
public class KPIFillInBean {
	private String id;//KPI 子表ID
	private String bindId;
	private String gwmc;//岗位名称
	private String bkhr;//被考核人
	private String kpibm;//kpi编码
	private String zblb;//类别
	private String zbmx;//明细
	private String zsmb;//模板
	private String mc;//参数名称
	private String lx;//参数类型
	private String sm;//参数说明
	private String khr;//考核人
	private double csz;//参数值
	
	public KPIFillInBean(){
	}
	/**
	 * 初始子表
	 * @param id
	 * @param gwmc
	 * @param bkhr
	 * @param kpibm
	 * @param zblb
	 * @param zbmx
	 * @param zsmb
	 * @param mc
	 * @param sm
	 * @param khr
	 */
	public KPIFillInBean(
			String id,String gwmc,String bkhr,
			String kpibm,String zblb,String zbmx,
			String zsmb,String mc,String sm,String khr){
		this.id=id;
		this.gwmc=gwmc;
		this.bkhr=bkhr;
		
		this.kpibm=kpibm;
		this.zblb=zblb;
		this.zbmx=zbmx;
		
		this.zsmb=zsmb;
		this.mc=mc;
		this.sm=sm;
		this.khr=khr;
	}
	/**
	 * 回填使用
	 * @param id
	 * @param mc
	 * @param csz
	 */
	public KPIFillInBean(String id,String mc,double csz){
		this.id=id;
		this.mc=mc;
		this.csz=csz;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getBindId() {
		return bindId;
	}
	public void setBindId(String bindId) {
		this.bindId = bindId;
	}
	public String getGwmc() {
		return gwmc;
	}
	public void setGwmc(String gwmc) {
		this.gwmc = gwmc;
	}
	public String getBkhr() {
		return bkhr;
	}
	public void setBkhr(String bkhr) {
		this.bkhr = bkhr;
	}
	public String getKpibm() {
		return kpibm;
	}
	public void setKpibm(String kpibm) {
		this.kpibm = kpibm;
	}
	public String getZblb() {
		return zblb;
	}
	public void setZblb(String zblb) {
		this.zblb = zblb;
	}
	public String getZbmx() {
		return zbmx;
	}
	public void setZbmx(String zbmx) {
		this.zbmx = zbmx;
	}
	public String getZsmb() {
		return zsmb;
	}
	public void setZsmb(String zsmb) {
		this.zsmb = zsmb;
	}
	public String getMc() {
		return mc;
	}
	public void setMc(String mc) {
		this.mc = mc;
	}
	public String getLx() {
		return lx;
	}
	public void setLx(String lx) {
		this.lx = lx;
	}
	public String getSm() {
		return sm;
	}
	public void setSm(String sm) {
		this.sm = sm;
	}
	public String getKhr() {
		return khr;
	}
	public void setKhr(String khr) {
		this.khr = khr;
	}
	public double getCsz() {
		return csz;
	}
	public void setCsz(double csz) {
		this.csz = csz;
	}
}
